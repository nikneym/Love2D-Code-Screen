function love.conf(t)
	t.window.width          = 640
	t.window.height         = 480
	t.window.fullscreen     = false
	t.console               = true
	t.window.title          = "empty"
	t.window.fullscreentype = "desktop"
	t.window.resizable 		= true
	t.window.x              = 700
	t.window.y              = 220
end